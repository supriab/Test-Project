public class Compare {
//==,!=
    public static void main(String[] args){

        System.out.println(6 > 3);//true
        System.out.println(3>6);//false
        System.out.println(6==3);//false
        System.out.println(6==6);//true
        System.out.println(7<10);//true
        System.out.println(6!=7);//true
        System.out.println(6<6);//false
        System.out.println(6<=6);//true
        System.out.println(6>6);//false
        System.out.println(6<=6);//true
        System.out.println(6>=10);//false



    }
}
