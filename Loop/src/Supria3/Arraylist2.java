package Supria3;

import java.util.ArrayList;

public class Arraylist2 {
    public static void main(String[] args) {
        ArrayList<String> colors = new ArrayList<>();
        colors.add("red");
        colors.add("blue");
        colors.add("green");
        colors.add("black");
        colors.add("pink");
        colors.add("yellow");
        for(int i = 0; i < colors.size(); i++) {
            System.out.println(colors.get(i));
        }




    }


}
